const validPasswords = puzzleInput.filter(function (item) {
    const min = item.strength[0];
    const max = item.strength[1];

    const numberOfTimesLetterOccurs = item.password
        .split("")
        .reduce(function (prev, cur) {
            if (cur === item.letter) {
                return (prev += cur);
            } else {
                return prev;
            }
        }, "").length;

    if (numberOfTimesLetterOccurs >= min && numberOfTimesLetterOccurs <= max) {
        return item;
    }
});

const part1 = validPasswords.length;

